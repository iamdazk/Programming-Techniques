using namespace std;

float x;
unsigned int n;

double result();

int main() {

    cin >> x >> n;

    cout << fixed << setprecision(2) << result();

    return 0;
}

double result(){

    int sum = 1;
    double temp = x;
    double S = -x;
    for(int i = 2; i <= n ; i++){
        temp = (temp * x * sum)/(sum + i);
        (i % 2 == 0) ? S+= temp : S -= temp;
        sum += i;
    }

    return S;
}